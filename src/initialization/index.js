let log = require('mue-core/modules/log')(module);
let _ = require('lodash');
let feedManager = require('../modules/feed-manager');

exports.init = function () {
    if (!environment.isTest()) {

        // step by step add feed to the system
        _.reduce(feedUrls, function (initialPromise, nextFeedUrl) {
            return initialPromise.then(function () {
                return feedManager.trackFeed(nextFeedUrl);
            }).catch(function () {
                return Promise.resolve()
            });
        }, Promise.resolve());
    } else {
        log.info('Skip Rss initialization');
    }
};